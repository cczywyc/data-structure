package com.wangyc.dataStructure.tree;

/**
 * @author cczywyc
 *
 * binary tree node
 */

public class TreeNode {
    /**
     * the node value
     */
    int val;

    /**
     * left son node point
     */
    TreeNode left;

    /**
     * right son node point
     */
    TreeNode right;

    TreeNode(int x) {
        val = x;
    }
}
